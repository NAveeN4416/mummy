وبالنظر إلى مسيرة المملكة في مجال الإعلام سنشعر بالانبهار من هذه التجربة المميزة والفخر بها إذ بدأت عام 1924 م / 1343 هـ بتأسيس جريدة " أم القرى " وكانت أول جريدة رسمية تصدر في المملكة العربية السعودية . ثم في عام 1350هـ تأسست جريدة صوت الحجاز وكانت جريدة أسبوعية مستقلة يمتلكها محمد صالح نصيف وعبد الوهاب آشي، وتغير اسمها بعد ذلك إلى جريدة " البلاد السعودية " في عام 1370هـ. ثم في عام 1355هـ صدرت مجلة المنهل على يد عبد القدوس الأنصاري ، وفي نفس العام صدرت جريدة المدينة المنورة.ثم توالت الصحف في الظهور، ففي عام 1377هـ صدرت جريدة عرفات وأصدرها عبد الحي حسن قزاز وفيما بعد اندمجت مع جريدة البلاد السعودية وأصبح أسمها " البلاد ". وتبع ذلك صحف أخرى تحولت إلى مؤسسات إعلامية سعودية تقود العمل الإعلامي.

ومن أوائل المجلات السعودية مجلة الحج التي صدرت في عام 1366هـ وهي مجلة شهرية تصدر عن وزارة الحج والأوقاف، ومن ثم استمرت المجلات والصحف بمختلف أسماءها وأنواعها وأبرزها حراء والندوة والأضواء و عكاظ وقريش وأخبار العالم الإسلامي والرائد وكلمة الحق والرياضي ، وحتى يومنا هذا هناك عدد من هذه الصحف مازالت تصدر بشكل دوري ومتطور يتناسب مع عصر الانترنت.

مسيرة تحتفي بالإعلام المسموع: نشأة الإذاعة السعودية

شهد عام 1949م/1368هـ تأسيس أول إذاعة في المملكة ، و بعد مرور عدة سنوات وتحديدًا عام 1955م / 1374هـ تأسست المديرية العامة للإذاعة والصحافة لتكونالنواة الحقيقية للإعلام السعودي، وكانت المديرية حينها في نفس مكانة وزارة الإعلام في الوقت الحالي، حيث كانت مسؤولة مسؤولية كاملة عن كل ما يتعلق بالتنظيم والإشراف وتنسيق المهمات الخاصة بالنشر وبالإذاعة داخل المملكة العربية السعودية ، وكانت تحتوي على ثلاث أقسام فقط ؛ قسم الصحافة وقسم البث الإذاعي وقسم النشر.

مع التطور الاقتصادي والحضاري في المملكة بدأت الحياة تختلف وتتطور مما أدى إلى ضرورة التوسع في الخدمات الإعلامية، وهو ما جعل الحكومة السعودية تصب اهتماما أكبر على الإعلام وحينها صدرالأمر بتحويل المديرية العامة للإذاعة والصحافة والنشر إلى وزارة الإعلام بموجب القرار الملكي رقم 57 لعام 1962م/1382هـ. وكان أول وزير للإعلام هو الوزير عبد الله بن عمر بلخير.

منذ ذلك الوقت لم يتوقف العمل في وزارة الإعلام السعودية واستمرت مسيرتها بخطى ثابتة من أجل تطوير العمل وتقديم خدمات أفضل للجماهير تلبية للمهمة العظيمة المنوطة بها لتؤكد دائمًا على مسؤوليتها الإعلامية تجاه الوطن والمواطن.

بدأ الإعلام السعودي بعد هذا القرار يتخذ خطوات كبيرة وواثقة لتأسيس بنية تحتية قوية من أجل تقديم مضمون إعلامي مميز يحقق الريادة على يد كفاءات سعودية.

وتتواصل المسيرة الإعلامية بنشأة التلفزيون السعودي

بدأت المرحلة التلفزيونية الأولى بتجهيز البنية الأساسية وكوادر العمل الإعلاميحيث عمدت الوزارة إلى إرسال عدد من البعثات الدراسية لدراسة مختلف أنواع التخصصات المطلوبة للعمل الإعلامي. وفي عام 1967م بدأت المرحلة الثانية وفيها أنشئت ثلاث مرسلات لإيصال البث التلفزيوني إلى مكة المكرمة والطائف وجدة، وبدأ التوسع في إنشاء محطات أخرى في عدد من الأماكن والمحافظات داخل المملكة وذلك بهدف إيصال البث إلى كل المحافظات السعودية، وقد بدأت الوزارة كذلك في إنشاء محطات تقوية ومراكز بث تعتمد على الأقمار الصناعية، وحتى عام 2005 تم تأسيس أكثر من مئة وعشرين محطة تقوية بث لتغطي أكثر من 90% من مساحة المملكة.

في عام 1978 دخلت المملكة في عصر البث الفضائي عن طريق استئجار قناة كاملة على القمر الصناعي انتلسات وذلك من أجل ربط المحطات التلفزيونية العامة مع محطة تلفزيون الرياض، وفي عام 1985 م انطلق أول قمر صناعي عربي ومنذ ذلك الحين بدأت القنوات السعودية تنتشر ليس فقط محليًا ولكن على مستوى الدول العربية وبعض دول جنوب أوروبا ووسط أفريقيا.

سبق سعودي في عهد الإنترنت 

لم تتوقف المسيرة الإعلامية السعودية، بل إنها ظلت تواكب متغيرات الآلة الإعلامية، فمع انتشار الإنترنت ومواقع التواصلالاجتماعي تمكن الإعلام السعودي من أن  يربط المحتوى الذي يقدمه باهتمامات الجماهير، وقد تطور الإعلام السعودي واتخذ العديد من الأشكال فنجد السعوديين قادة التأثير في المواقع الإلكترونية والمدونات وكذلك البرامج التفاعلية والتطبيقات على الهواتف الذكية،  ففي تقرير مواقع التواصل الاجتماعي 2018 الصّادر عن مؤسّسة "Crowd Analyzer"، فإنّ بالمملكة العربيّة السّعوديّة أعلى عددٍ من مستخدمي تويتر النّشِطين في المنطقة، ووفقا لتقرير نشرته العربية فإن ٩ من ١٠ أشخاص في السعودية يملكون هواتف ذكية مما يعني وصولا للإنترنت، ويذكر التقرير أن السعوديين أكثر الشعوب استهلاكا للإنترنت وفق إحصائيات قوقل، وما يزالحضورهميتزايد يومًا بعد يوم ليثبت أن الإعلام السعودي يستحق المكانة التي يحتلها، ومن المتوقع أن يصل إلى مكانة أفضل وأعرق عن طريق العمل المستمر والمجتهد.
