 <p class="viewd-para"> أم القرى
تعد أم القرى أول صحيفة صدرت في المملكة العربية السعودية وذلك عام 1343هـ/1924م، وبدأت منوعة رغم أنها رسمية، واستمرت على ذلك إلى ما بعد الحرب العالمية الثانية حين تحولت إلى صحيفة رسمية بالكامل، وهي مستمرة إلى اليوم، ثم تلاها "صوت الحجاز" حيث صدرت عام 1932م والتي تحولت فيما بعد إلى "البلاد" وهي تصدر إلى اليوم. بينما صدر أول نظام للمطبوعات عام 1347هـ/ 1929م. صدر نظام المؤسسات الصحفية 1383هـ/1964م. بينما تأسست الإذاعة السعودية عام 1368هـ/1949م. وتلاها التلفزيون حيث تأسس التلفزيون السعودي عام 1385هـ/1965م. ثم تأسست وكالة الأنباء بعد التلفزيون بخمس سنوات وذلك في عام 1970م.
الصحافة
ومرت الصحافة بثلاث مراحل: صحافة الأفراد وكانت صحافة رأي تعبر عن مواقف أصحابها. 1932م ـ 1959.
 صحافة الدمج؛ كان ما يقارب 40 صحيفة تصدر آنذاك وتم دمجها بحيث يصبح لكل مدينة صحيفة واستمرت لما يقارب الأربع سنوات من 1959 - 1963م، لتأتي صحافة المؤسسات بعد ذلك، حين صدر مرسوم ملكي بتاريخ 10-1-1964م بالموافقة على نظام المؤسسات الأهلية.
الإذاعة 
أنشئت أول محطة إذاعية سعودية في مدينة جدة، وبدأ إرسالها يوم 9 ذي الحجة 1368هـ الموافق 1 أكتوبر 1949م - وهو يوم الوقوف بعرفة - بكلمة ألقاها الأمير (الملك) فيصل بن عبد العزيز. ويعد ميلاد الإذاعة السعودية في عهد مؤسس المملكة وموحدها جلالة الملك عبد العزيز يوم الثلاثاء 23 رمضان 1368هـ الموافق 19 يوليو 1949م. وبدأ بث "إذاعة نداء الإسلام من مكة المكرمة" 11/5/ 1965م. وكانت تحت مسمى برنامج صوت الإسلام ثم تغير إلى نداء الإسلام عام 1971م.  بينما بدأ البث الإذاعي من إذاعة الرياض يوم الأحد 1 رمضان عام 1384هـ الموافق 3 يناير 1965م، وبدأ بث البرنامج العام مستقلاً من إذاعة الرياض، والبرنامج الثاني من إذاعة جدة يوم الخميس 1 شوال 1399هـ الموافق 23 أغسطس 1965م. وبدأت الإذاعة تبث باللغة الإنجليزية من جدة يوم 1/1/1965م. 
التلفزيون
في عام 1383هـ الموافق 1963م أقر مجلس الوزراء مشروعاً بإنشاء التليفزيون في المملكة العربية السعودية على مرحلتين مرحلة تبدأ ببناء محطتي إرسال تليفزيوني مؤقتتين في كل من الرياض وجدة. والثانية جاءت بعد المرحلة الأولى بعدة سنوات لإنشاء نظام تليفزيوني على أسس أكثر تطوراً، ويكون محققاً للآمال والطموحات التي يتطلع إليها المسؤولون؛ ففي 19 من ربيع الأول من عام 1385هـ الموافق 17 من يوليو 1965م خرجت أول إشارة بث من محطتي تليفزيون الرياض وجدة، وبلغت التكاليف حوالي عشرة ملايين ريال سعودي لكل محطة. وفي عام 1387هـ تم افتتاح محطة تليفزيون المدينة المنورة، وفي عام 1388هـ تم افتتاح محطة تليفزيون القصيم، وعام 1389هـ تم افتتاح محطة تليفزيون الدمام.

وكالة الأنباء:
