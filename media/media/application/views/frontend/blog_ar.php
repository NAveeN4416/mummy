بينما حمل المسار الثاني: إعادة هيكلة الإذاعة والتلفزيون ووكالة الأنباء السعودية ففي عام 1433هـ / 2012م صدر تنظيم بقرار من مجلس الوزراء بتحويل الإذاعة والتلفزيون معاً إلى هيئة عامة، وتنظيم آخر مماثل لوكالة الأنباء السعودية. ونتج عن هذا التغيير تحويل جميع الموظفين الماليين والإداريين والفنيين الذين يرتبط عملهم بالإذاعة والتلفزيون ووكالة الأنباء السعودية وفصل البنود المالية من ميزانية الوزارة ووضعها في ميزانية الهيئتين، كما أصبح لكل هيئة من الهيئتين مجلس إدارة برئاسة وزير الثقافة والإعلام الذي يمثلهما في مجلس الوزراء، ومن بين مسؤوليّات مجلس الإدارة إقرار الهيكل التنظيمي واللوائح المالية والوظيفية والمهنية والفنية التي تقترحها الهيئتان، وهنا أصبح ارتباط الهيئتين بالوزارة ارتباطاً إسميّاً يتمثّل بشخص الوزير رئيس مجلس إدارتهما، أما جهاز الوزارة فلا علاقة له بعملهما.

المسار الثالث: نموّ قطاع الإعلام الأهلي أبرز مظاهر نموّ القطاع الأهلي في مجال الإعلام التالي:
1 - الترخيص لخمس من الإذاعات الأهلية من قبل وزارة الثقافة والإعلام (1431هـ / 2010م).
2 - تأسيس أكاديمية أحمد بن سلمان للإعلام التطبيقي من قبل المجموعة السعودية للأبحاث والتسويق، وكانت فكرتها بدأت في عام 1424هـ / 2004م حين قدمت دورات في مجال الطباعة والألوان، ثم انطلقت بعد ثلاثة أعوام بتقديم البرامج العلمية، وكان التدشين الرسمي للأكاديمية في العام التالي (1428هـ / 2008م).
3 - ولادة العديد من القنوات الإذاعية والتلفزيونية المحلية التي أسّست مراكز إنتاج في الداخل وبثّ في الخارج، وظهرت تلك القنوات دون الحصول على الترخيص المعتاد من الجهات المختصة، وذلك على اعتبار أن البث لا ينطلق من داخل المملكة، متّبعةً في هذا الأمر ما كانت المطبوعات غير المرخصة تطبّقه من حيث تحضير المادة محليّاً وطباعتها خارجيّاً.
4 - تأسيس قسم للإعلام والقانون في جامعة اليمامة الأهلية بالرياض (1436هـ / 2015م).
5 - قيام المجموعة السعودية للأبحاث والتسويق بوصفها مجموعة قابضة لمجموعة من الشركات والإصدارات السعودية والأجنبية، وقد بادرت واحدة من شركاتها (الشركة السعودية للأبحاث والنشر المسجلة في بريطانيا) في عام 1429هـ / 2008م إلى إنشاء مجلس للأمناء على غرار بعض دور الصحف الأجنبية بهدف حماية التحرير من سطوة التأثير التجاري، وهو أول تكوين من نوعه في المملكة وفي البلاد العربية.

المسار الرابع: إعادة تنظيم جهاز الإعلام المركزي الرسمي يمكن حصر أبرز ما شهده هذا المسار في الفروع الآتية:
1 - دمج أجهزة الثقافة والإعلام في وزارة واحدة، جاء ذلك ضمن حزمة من الإصلاحات الإدارية في أوامر ملكية عدة (1423هـ / 2003م) ونتج عن هذا الدمج فصل جميع الأقسام وقطاعات الثقافة الموجودة في الأجهزة الحكومية الأخرى وإلحاقها بوزارة الثقافة والإعلام.
2 - إلغاء المجلس الأعلى للإعلام، جاء ذلك ضمن الأوامر الآنفة الذكر، والمعروف أن المجلس بدأ عام 1387هـ / 1967م في شكل لجنة للتخطيط الإعلامي، ثم تطوّرت فكرته حتى أصبح يسمى المجلس الأعلى للإعلام برئاسة الأمير نايف بن عبد العزيز بوصفه رجل دولة، منذ عام 1401هـ / 1981م وحتى سنة إلغائه، وبغض النظر عن مدى فاعلية المجلس خلال مسيرته، إلا أنه كان - بموجب لائحته - جهةً تنظيمية (تشريعية نهائية تماثل صلاحية مجلس الوزراء)، كما كان يمثّل الحكم المحايد للفصل في التظلّمات التي تقدّم من الإعلاميين ومنها ما كان ضد وزارة الثقافة والإعلام نفسها، فيحكم المجلس بشأنها بما يراه موضوعيّاً منصفاً، حتى وإن كان مناقضاً لوجهة نظر جهة حكومية طرف في الموضوع كوزارة الثقافة والإعلام.
3 - تأسيس هيئة الإعلام المرئي والمسموع (صدرت بمرسوم ملكي عام 1433هـ / 2012م) بهدف تنظيم نشاط البث الإعلامي المرئي والمسموع وفق السياسة الإعلامية، ووضع أُطر للترخيص للبث بما يستوعب الاتجاهات التجارية والتقنية المتطورة، وربما تكون الوزارة قد استحدثت فكرة هذه الهيئة لتكون عوضاً عن المجلس الأعلى للإعلام، لكن الفارق كبير بينهما كما يتضح من مقارنة نظام الهيئة مع لائحة المجلس، وقد يكون التنظير الذي لازم فكرتها والازدواجية التي وقعت فيها مع بعض قطاعات الوزارة وبخاصة الإعلام الداخلي، انعكسا على عدم فاعليتها منذ إنشائها وحتى الآن، ومن يطّلع على نظامها يجد أنه أمام نصوص غامضة.

المسار الخامس: تحديث بعض الأنظمة الإعلامية القائمة تمّ خلال الأعوام الخمسة عشر الماضية تحديث مجموعة من أنظمة الإعلام القائمة على النحو التالي:
1 - نظام المطبوعات والنشر، صدر بمرسوم ملكي (1421هـ / 2001م) وهو في نسخته الجديدة يُعدّ الخامس في تاريخ الإعلام، وكان من أهم ما تضمنه من تجديد هو السماح بقيام جمعيات (نقابات) مهنية في مجال الإعلام (المادة 44).
2 - نظام المؤسسات الصحفية (صدر بمرسوم ملكي عام 1422هـ / 2001م) وهو الإصدار الثاني في تاريخ المؤسسات الصحفية، وتضمّن تعديلات من أبرزها الاستناد إلى السياسة الإعلامية، والاعتراف بمجالس إدارة المؤسسات بعد أن كانت الجمعيات العمومية في النظام السابق هي الجهة الوحيدة الحاكمة للمؤسسات، كما تضمن النظام زيادات في رأس المال وبعض الشروط التي تكفل تحسين أداء المؤسسات، وقد أبقى النظام لوزارة الثقافة والإعلام دون غيرها حق التدخل في الشؤون التحريرية والتنظيمية لصحف المؤسسات.
3 - نظام حماية حقوق المؤلف، صدر بمرسوم ملكي (1424هـ / 2004 م) ليحلّ محل النظام القديم الصادر عام 1410هـ / 1990م وقد صدرت هذه الصيغة الجديدة للنظام التزاماً باتفاقيات دولية، وقد قدمته وزارة الخارجية إلى مجلس الوزراء، وطلب النظام من وزارة الثقافة والإعلام إصدار لائحته التنفيذية والإشراف على تنفيذه، وهذا النظام جزءٌ من منظومة الملكية الفكرية التي تشترك في تنفيذها كل من مدينة الملك عبد العزيز للعلوم والتقنية من خلال نظام براءة الاختراع، ووزارة التجارة من خلال تطبيق نظام العلامات التجارية، وغيرهما.

المسار السادس: مؤسسات المجتمع المدني والعلمي، حيث سجل هذا المسار صدور قرارات بتأسيس عدة بدايات لمؤسسات المجتمع المدني والعلمي، وهي:
1 - جمعية الإعلام والاتصال، وهي جمعية علمية منبثقة عن جامعة الملك سعود (1421هـ / 2001م) ويصدر عنها مجلة نصف سنوية.
2 - جمعية الناشرين السعوديين (1424هـ / 2004م) صدرت لائحتها من وزارة الإعلام، وهي جمعية مهنية منبثقة من نظام المطبوعات والنشر، ولها نشاط ملحوظ.
3 - هيئة الصحفيين السعوديين، وهي جمعية مهنية تأسست عام 1425هـ / 2004م بموجب المادة (27) من نظام المؤسسات الصحفية.
4 - الجمعية السعودية للعلاقات العامة والإعلان (1431هـ / 2011م) وهي جمعية علمية انبثقت عن لائحة الجمعيات العلمية في الجامعات السعودية، بادرت إلى تأسيسها جامعة الامام محمد بن سعود الإسلامية، وما تزال في مرحلة البدايات.
5 - جمعية الإعلام الإلكتروني (1434هـ / 2013م) وهي جمعية مهنية صدرت لائحتها من وزارة الثقافة والإعلام بناءً على مواد نظام المطبوعات والنشر، وما تزال في أول الطريق.
6 - الجمعيّة السعودية لكتّاب الرأي (1434هـ / 2013م) وقد تشكّلت بموجب نظام المطبوعات والنشر، وما تزال في مراحلها التأسيسية المبكّرة.
7 ـ جمعية إعلاميون عام 2018م.

المسار السابع: في المؤسسات الصحفية، حيث تشهد المطبوعات الصحفية في السنوات القليلة الماضية بعض المتغيّرات السلبية نتيجة ضعف موارد التوزيع والإعلان ومنافسة الصحافة الإلكترونية وارتفاع تكلفة الطباعة والورق، فتوقفت مطبوعات عدة، بدأت بصحيفة «المسائية» من مؤسسة الجزيرة الصحفية (1422هـ / 2001م) على وعدٍ بتحويلها إلى صحيفة صباحية موجهة للأسرة، ثم لحقتها صحيفة «رياض ديلي» من مؤسسة اليمامة الصحفية (1424هـ / 2004م) ومجلة «رؤى» من مؤسسة عكاظ الصحفية (1432هـ / 2011م) ومطبوعات أخرى.
ومن أبرز المتغيرات الأخرى في عالم المؤسسات الصحفية:
1 - قيام مؤسسة عسير الصحفية وصدور جريدة «الوطن» (1420هـ / 2000م).
2 - تحويل شركة الشرقية للطباعة والصحافة والإعلام إلى مؤسسة صحفية وصدور جريدة «الشرق» (1433هـ / 2011م).
3 - تحويل امتياز جريدة «الندوة» إلى جريدة «مكة» (1435هـ / 2014م).
4 - توسّع العديد من المؤسسات الصحفية في إحداث الأقسام النسوية للعمل في مجال التحرّير والتصحيح والتسويق، وأقدمت مؤسسة عكاظ الصحفية على تعيين أول رئيسة تحرير لصحيفة سعودية (سُمَيَّة جبرتي في جريدة «سعودي جازيت» بالإنجليزية 1435هـ / 2014م).

المسار الثامن: أقسام الدراسات الإعلامية، فقد كان من أبرز ما شهده الإعلام من متغيرات كَمِيَّة ونوعية خلال العقود الأربعة الماضية، هو استمرار إنشاء أقسام الإعلام في المؤسسات التعليمية الجامعية، فبعد جامعة الملك سعود والملك عبد العزيز والإمام محمد بن سعود الإسلامية وأم القرى قبل صدور الكتاب، تأسس منذ صدوره قسم للصحافة والإعلام في جامعة جازان (1430هـ / 2011م) وقسم الاتصال والإعلام في جامعة طيبة (1431هـ / 2012 م) وقسم للإعلام في جامعة الملك فيصل (1433هـ / 2013م) وقسم الاتصال وتقنية الإعلام في جامعة الدمام (1434هـ / 2014م) وقسم للإعلام في جامعة الملك خالد في العام نفسه، وتحوّل قسم الإعلام في جامعة الإمام محمد بن سعود الإسلامية إلى كلية للإعلام والاتصال (1434هـ / 2013م) وفُتح المجال في كثير من تلك الأقسام للدراسات العليا وللفتيات وجُهِّزت بما تحتاجه من تقنيات للتدريب، غير أن الملحظ المؤسف في هذا النمو المطّرد الذي أوصلها إلى عشرة، هو عدم مواكبة معظم هذه الأقسام للمأمول منها في حقول التوثيق والبحوث والتأليف في ميادين المعلومات والدراسات الإعلامية، وأن تركيزها على الإعلام البديل ما يزال محدوداً.



